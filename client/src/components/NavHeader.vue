<script>
import '../../static/css/base.css'
import Modal from '../components/Modal'
import { delCookie, getCookie } from '@/util/util'
export default {
    name: 'HelloWorld',
    props: ['isNowLogin'],
    components:{
        Modal
    },
    data () {
        return {
            userName:'',
            userPwd:'',
            isLogin:false,
            topName:'',
            toCartConfirm:false,
            cartNum:0
        }
    },
    created(){
        // this.searchCookie();
        this.checkLogin()
    },
    
    methods:{
        login(){
            this.$https.post('/users/login',{
                userName:this.userName,
                userPwd:this.userPwd
            }).then(res=>{
                console.log(res)
                this.isLogin=false;
                this.topName=res.data.data.userName
                this.$https.post('/users/getCartList').then(res=>{
                        this.cartNum=res.data.data.length
                    })
            })
        },
        searchCookie(){
            if(getCookie('userName')){
                this.topName=getCookie('userName')
            }
        },
        checkLogin(){
            this.$https.post('/users/checkLogin').then(res=>{
                if(res.data.code===0){
                    this.topName=res.data.data
                    this.$https.post('/users/getCartList').then(res=>{
                        this.cartNum=res.data.data.length
                    })
                }else{
                    // this.isLogin=true
                }
            })
        },
        logOut(){
            this.$https.post('/users/delLogin').then(res=>{
                this.topName=''
                this.$router.push({path:'/'})
            })
        },
        toCart(){
            this.$https.post('/users/checkLogin').then(res=>{
                if(res.data.code===0){
                    this.$router.push({path:'/cart'})
                }else{
                    this.toCartConfirm=true
                }
            })
        },
        toLogin(){
            this.toCartConfirm=false;
            this.isLogin=true;
        }
    }
}
</script>

